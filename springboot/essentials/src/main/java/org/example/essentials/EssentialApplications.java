package org.example.essentials;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;

// * 웹(Web server), WAS, 톰캣(Tomcat)이란?

// [웹 서버]
// 먼저 '웹 서버'는 브라우저의 요청을 받아 이미 만들어져 있는 정적인 파일(html, css, 이밎 등..)을 그대로 전달해주는 서버다.
// - 대표적인 웹 서버가 바로 아파치(Apache HTTP Server)와 Nginx다.
// - 정적 파일 전달, HTTPS 처리, 요청 분배(로드 밸런싱) 같은 일은 잘하지만,
// 자바 코드를 직접 실행해 결과를 만들어 내지는 못한다.

// - WAS(Web Application Server)
// 사용자가 브라우저로 요청을 보내면, 그 요청을 받아 우리가 작성한 자바 코드를 실행하고
// 그 결과(HTML, JSON 등)를 다시 응담으로 돌려 주는 '실행 환경' 이 바로 was다.
// - 즉 웹 서버가 못하는 '동적인' 처리를 요청마다 프로그램을 돌려 만들어 낸다.
// - 요청/응답 처리, 스레드 관리, 세션 관리 같은 서버 공통 기능을 대신 맡아 준다.
// - 실무에서는 아파치(웹 서버)를 앞단에 두어 정적 요소를 처리하고,
// 동적 요청만 뒷단의 WAS로 넘기는 구조를 함께 쓰기도 한다.

// [TOMCAT은 그런 WAS 중 가장 널리 쓰이는 오픈소스 구현체이다.]
// 자바의 서블릿(Servlet) 규격을 따르며, 스프링 MVC도 결국 이 서블릿 위에서 동작한다.
// - 톰캣은 정적 파일도 어느 정도 다룰 수 있어, 규모가 작으면 웹 서버 없이 톰캣만으로도 서비스가 가능하다.
// - 원래는 톰캣을 따로 설치하고 그 안에 우리 애플리케이션(WAR)을 넣어 실행했다.
// - 스프링부트는 이 톰캣을 라이브러리 형태로 애플리케이션 '안에' 내장했다.
// 그래서 별도 설치 없이 main() 실행만으로 서버가 함께 떠서 요청을 받을 수 있다.

// * HTTP 프로토콜이란
// HTTP(HyperText Transfer Protocol)는 브라우저(클라이언트)와 서버가 서로 데이터를 주고받을 때 지키는 '약속(규칙)'이다.
// - 통신은 항상 '요청(Request) -> 응답(Response)' 한 쌍으로 이루어진다.
// 클라이언트가 요청을 보내면 서버가 그에 대한 응담을 돌려 주는 식이다.
// - 요청에는 '무엇을'(URL), '어떻게'(메서드), 부가 정보(헤더), 본문(body)이 담긴다.
// - 응답에는 처리 결과를 나타내는 상대코드(200 성공, 404 없음, 500 서버 오류 등)와 실제 데이터(HTML, JSON 등)가 담긴다.
// - HTTP는 '무상태(stateless)' 프로토콜이라, 각 요청은 서로를 기억하지 못한다.
// 그래서 로그인 상태 유지 등을 위해 세션이나 토큰 같은 보조 장치를 함꼐 사용한다.

// * HTTP 메서드 - GET, POST, PUT, DELETE
// 메서드는 '이 요청으로 무엇을 하고 싶은지'를 나타내는 동사다.
// 데이터의 생성/조회/수정/삭제(CRUD)와 자연스럽게 짝지어진다.
// - GET : 데이터를 '조회'할 때 쓴다.
// 서버의 상태를 바꾸지 않으며, 값은 주로 URL뒤 쿼리 스트링에 담는다.
// - POST : 데이터를 '새로 생성'할 때 쓴다.
// 보낼 내용을 요청 본문에 담으며, 서버의 상태를 바꾼다.
// - PUT : 기존 데이터를 '수정(전체 교체)'할 때 쓴다.
// 같은 요청을 여러 번 보내도 결과가 같은 '멱등성'을 가진다.
// - DELETE : 데이터를 '삭제'할 때 쓴다.
// - 스프링에서는 @GetMapping, @PostMapping, @PutMapping, @DeleteMapping 으로
// 각 메서드 요청을 컨트롤러의 특정 메서드에 연결한다.

//* 멱등성
// 멱등성은 '같은 요청을 한 번 보내든 여러 번 보내든, 서버의 최종 상태가 똑같이 유지되는 성질'을 말한다.
// '여러 번 눌러도 결과가 그대로'다
// - GET : 멱등하다. 여러 번 조회해도 데이터가 바뀌지 않는다.
// - PUT : 멱등하다. 예를들어 회원 3번의 이름을 '홍길동'으로 바꿔라 를 10번 보내도, 겷과는 언제나 '이름이 홍길동'인 하나의 상태다.
// - DELETE : 멱등하다. 예를들어 회원 3번을 삭제하라 -> 여러 번 보내도 3번이 없는 상태는 동일하다.
// - POST : 멱등하지 '않다'. "회원을 새로 등록하라" 10번 보내면 10명 생겨 버린다. 보낼 때마다 상태가 계속 달라진다.
// 왜 중요한가?
// 네트워크 오류로 응답을 못 받아 요청을 '재시도'하는 상황이 생겼을 때,
// 이때 멱등한 요청은 여러 번 가도 안전하지만,
// 멱등하지 않은 POST는 중복 처리(예, 결제 두 번)가 될 수 있어 주의해야 한다.

// * 스프링부트 애플리케이션의 시작점


@SpringBootApplication
public class EssentialApplications {

    public static void main(String[] args) {
        SpringApplication.run(EssentialApplications.class, args);
    }

}
